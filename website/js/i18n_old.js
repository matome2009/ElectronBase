// スライダー機能
function initSlider() {
  const images = document.querySelectorAll('.slider-image');
  const prevBtn = document.querySelector('.slider-btn.prev');
  const nextBtn = document.querySelector('.slider-btn.next');
  const dotsContainer = document.querySelector('.slider-dots');
  
  if (!images.length || !prevBtn || !nextBtn || !dotsContainer) return;
  
  let currentSlide = 0;
  const totalSlides = images.length;
  
  // ドットを生成
  for (let i = 0; i < totalSlides; i++) {
    const dot = document.createElement('div');
    dot.className = 'slider-dot' + (i === 0 ? ' active' : '');
    dot.addEventListener('click', () => goToSlide(i));
    dotsContainer.appendChild(dot);
  }
  
  const dots = document.querySelectorAll('.slider-dot');
  
  function goToSlide(n) {
    images[currentSlide].classList.remove('active');
    dots[currentSlide].classList.remove('active');
    
    currentSlide = (n + totalSlides) % totalSlides;
    
    images[currentSlide].classList.add('active');
    dots[currentSlide].classList.add('active');
  }
  
  function nextSlide() {
    goToSlide(currentSlide + 1);
  }
  
  function prevSlide() {
    goToSlide(currentSlide - 1);
  }
  
  prevBtn.addEventListener('click', prevSlide);
  nextBtn.addEventListener('click', nextSlide);
  
  // 自動スライド（5秒ごと）
  setInterval(nextSlide, 5000);
}

// 現在の言語を取得
function getCurrentLanguage() {
  return localStorage.getItem('preferredLanguage') || 'ja';
}

// スライダーの画像URLを更新
function updateSliderImages() {
  const lang = getCurrentLanguage();
  const langFolder = lang === 'ja' ? 'ja' : 'en'; // 日本語以外は英語画像を使用
  
  const images = document.querySelectorAll('.slider-image');
  images.forEach((img, index) => {
    const imageNumber = index + 1;
    // Firebase Storageの正しいURL形式（バケット名: ticketsystem-4bc88.firebasestorage.app）
    const imageUrl = `https://firebasestorage.googleapis.com/v0/b/ticketsystem-4bc88.firebasestorage.app/o/img%2F${langFolder}%2F${imageNumber}.png?alt=media`;
    img.src = imageUrl;
    img.alt = `Screenshot ${imageNumber}`;
    
    // デバッグ用
    console.log(`Loading image ${imageNumber} (${langFolder}):`, imageUrl);
  });
}

// 言語変更イベントリスナーでスライダー画像も更新
window.addEventListener('languageChanged', (e) => {
  if (document.querySelector('.slider-image')) {
    updateSliderImages();
  }
});


